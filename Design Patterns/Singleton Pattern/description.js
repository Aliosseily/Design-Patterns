// Singleton Pattern

/*
Singleton Pattern is a way of creating a single object that is shared among a bunch of 
different resources through out your application without having to re-create that object or
losing the information inside of it.
all the state of that object, the variables and methods all of  those are shared among all
these different other objects that are using it, and there is only one source of information
for this object and there is only a single type of this object created 
that's why it's called singleton because your application will only and can only have one
type of this object instantiate it at a time, and all places that use this object share that
one single instance of the object.
*/
